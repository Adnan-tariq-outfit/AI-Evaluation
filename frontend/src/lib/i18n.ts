const translations: Partial<Record<LanguageCode, TranslationDict>> = {
  en,
  es,
  ar,
  ur,
};

const DEFAULT_LANGUAGE: LanguageCode = "en";

export function isLanguageCode(value: string): value is LanguageCode {
  return value in languageMeta;
}

export function resolveLanguage(value?: string | null): LanguageCode {
  if (!value) return DEFAULT_LANGUAGE;
  return isLanguageCode(value) ? value : DEFAULT_LANGUAGE;
}

export function getDirection(language: LanguageCode): "ltr" | "rtl" {
  return languageMeta[language]?.dir ?? "ltr";
}

export function translate(
  language: LanguageCode,
  key: string,
  values?: TranslationValues,
): string {
  const locale = translations[language] ?? {};
  let template = locale[key] ?? en[key] ?? key;
  if (!values) return template;
  Object.entries(values).forEach(([name, value]) => {
    template = template.replaceAll(`{{${name}}}`, String(value));
  });
  return template;
}
